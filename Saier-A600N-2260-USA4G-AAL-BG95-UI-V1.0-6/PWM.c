void PWMGVAL1(uint16 pwm_val)	 //马达的PWM值
{ 
	if(pwm_val>=0x03FF)
	{
		pwm_val=0x03FF;
	}
	PWMDATA.Val = pwm_val;
	_stm0al = PWMDATA.BYTE.BYTE_L;
	_stm0ah = PWMDATA.BYTE.BYTE_H;
}


/******************************************************************/
/******************************************************************/
void STP_PUMP_ON()
{
	_pas15 = 1;	//pa6==stp 
	_pas14 = 0;
	_st0on = 1;	
	
}
/******************************************************************/
/******************************************************************/
void STP_PUMP_OFF()
{
	_pas15 = 0;	//pa6==IO 
	_pas14 = 0;
	_pac6 = 0;
	_pa6 = 0;
	_st0on = 0;	
	
}




/******************************************************************/
/******************************************************************/
void STP_VALUE_ON()
{
	_pas13 = 0; //pa5== STP
	_pas12 = 1;	
	_st0on = 1;
	
}

/******************************************************************/
/******************************************************************/
void STP_VALUE_OFF()
{
	_pas13 = 0; //pa5== IO
	_pas12 = 0;	
	_pac5 = 0;
	_pa5 = 0;	
	_st0on = 0;
	
}	
